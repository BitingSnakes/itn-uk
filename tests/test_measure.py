import pytest

from ukr.wfst import normalize


@pytest.mark.parametrize("spoken,expected", [
    ("нуль цілих одна десята відсотка", '0.1 %'),
    ("мінус п'ять цілих одна десята відсотка", '-5.1 %'),
    ("п'ять відсотків", '5 %'),
    ("сто п'ять відсотків", '105 %'),
    ("сто відсотків", '100 %'),
])
def test_percent(spoken, expected):
    assert normalize(spoken) == expected


@pytest.mark.parametrize("spoken,expected", [
    ("сто двадцять кілометрів", '120 км'),
    ("двадцять два метри", '22 м'),
    ("п'ять сантиметрів", '5 см'),
    ("три міліметри", '3 мм'),
    ("мінус п'ять цілих три десятих кілограма", '-5.3 кг'),
    ("п'ятдесят грамів", '50 г'),
    ("три тонни", '3 т'),
    ("два літри", '2 л'),
    ("сто мілілітрів", '100 мл'),
    ("сорок гектарів", '40 га'),
    ("десять секунд", '10 с'),
    ("п'ять процентів", '5 %'),
])
def test_units(spoken, expected):
    assert normalize(spoken) == expected


@pytest.mark.parametrize("spoken,expected", [
    ("шістдесят кілометрів на годину", '60 км/год'),
    ("дев'ять цілих вісім десятих метра за секунду", '9.8 м/с'),
    ("двадцять п'ять градусів за цельсієм", '25 °C'),
    ("мінус сім градусів", '-7 °'),
    ("п'ятсот гігабайт", '500 ГБ'),
    ("шістнадцять гігабайтів", '16 ГБ'),
    ("два мегабайти", '2 МБ'),
    ("сто двадцять вісім кілобайт", '128 КБ'),
    ("два терабайти", '2 ТБ'),
    ("сто ват", '100 Вт'),
    ("два кіловати", '2 кВт'),
    ("триста кіловат-годин", '300 кВт·год'),
    ("двісті двадцять вольт", '220 В'),
    ("п'ять амперів", '5 А'),
    ("п'ятдесят герців", '50 Гц'),
    ("два мегагерци", '2 МГц'),
    ("три гігагерци", '3 ГГц'),
    ("тридцять два децибели", '32 дБ'),
    ("двісті кілокалорій", '200 ккал'),
    ("п'ятсот міліграмів", '500 мг'),
])
def test_extended_units(spoken, expected):
    assert normalize(spoken) == expected
